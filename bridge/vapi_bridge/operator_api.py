        _check_rate(api_key)
        _reason = (reason or "").strip()
        if len(_reason) < 10:
            raise HTTPException(
                422, "reason must be at least 10 characters (operator audit field)"
            )

        import time as _t236snap
        from .corpus_snapshot import (
            agent_root_from_hex,
            compute_corpus_commitment,
            compute_wiki_snapshot_hash,
        )
