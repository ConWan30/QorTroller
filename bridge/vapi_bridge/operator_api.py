    def _check_key(api_key: str) -> None:
        """Raise HTTP 503/403 if key is missing or wrong."""
        if not cfg.operator_api_key:
            raise HTTPException(503, "Operator API not configured (OPERATOR_API_KEY not set)")
        if not hmac.compare_digest(api_key, cfg.operator_api_key):
            raise HTTPException(403, "Invalid API key")

    def _check_rate(api_key: str) -> None:
        """Raise HTTP 429 if the api_key has exceeded the sliding-window rate limit (Phase 36)."""
        if not _limiter.is_allowed(api_key):
            raise HTTPException(
                status_code=429,
                detail="Rate limit exceeded",
                headers={"Retry-After": "60"},
            )

    def _check_read_key(x_api_key: str) -> None:
        """Phase 224 W1 fix: for read-only /agent/* status endpoints using x-api-key header.

        Validates the key when OPERATOR_API_KEY is configured (fail-open when not configured,
        so dev environments without a key still work). Always applies rate limiting.
        Returns 403 when key is wrong, never 503 (status endpoints are read-only).
        """
        if cfg.operator_api_key:
            if not hmac.compare_digest(x_api_key, cfg.operator_api_key):
                raise HTTPException(403, "Invalid x-api-key header")
        if not _limiter.is_allowed(x_api_key):
            raise HTTPException(
                status_code=429,
                detail="Rate limit exceeded",
                headers={"Retry-After": "60"},
            )

    def _sign(device_id: str, eligible: bool, ts: int) -> str:
        """Produce HMAC-SHA256 signature over device_id:eligible:timestamp."""
        msg = f"{device_id}:{int(eligible)}:{ts}".encode()
        return hmac.new(
            cfg.operator_api_key.encode(), msg, hashlib.sha256
        ).hexdigest()

    def _gate_response(device_id: str) -> dict:
        """Build a signed gate response dict for one device."""
        ts = int(time.time())
        checkpoint = store.get_last_phg_checkpoint(device_id)
        credential = store.get_credential_mint(device_id)
        score = checkpoint["last_committed_score"] if checkpoint else 0
        eligible = score > 0
        cred_id = credential["credential_id"] if credential else None
        sig = _sign(device_id, eligible, ts)
        return {
            "device_id":       device_id,
            "eligible":        eligible,
            "cumulative_score": score,
            "credential_id":   cred_id,
            "timestamp":       ts,
            "sig":             sig,
        }

